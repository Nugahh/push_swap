void    push_b(int *sa, int *sb, int size)
{
        int     i;

        i = 0;
        if (sa == 0)
                return;
        while (size > 0)
        {
                ft_swap(&sb[size], &sb[size - 1]);
                size--;
        }
        sb[0] = sa[0];
}

